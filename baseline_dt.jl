data = CSV.read("new_smiles.csv", DataFrame)

# ╔═╡ 9feee4af-80f6-4979-a1af-cbc5232a82b1
toxicity = data[:, "Outcome"]

# ╔═╡ 7d21181a-5177-48c2-8256-c242ae3130fc
